// switch case menerima keyword untuk dibandingkan (num/str)

const job = 'Designer'

switch (job) {
    case 'Gamer':
      console.log('You live to play')
    break
    case 'Chef':
      console.log('Can I have a steak?')
    break
    case 'Designer':
      console.log('Do NOT do it for exposure')
    break
    default:
      console.log('what do you do?')
}