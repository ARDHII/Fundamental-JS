// Comparison operators akan menghasilkan Boolean
// TIDAK BERLAKU UNTUK OBJECT//

// == perbandingan tanpa melihat tipe data (equal opertator)
console.log(5 == 5) // true
console.log(5 == '5') // true

// === perbandingan melihat tipe data (Strict Equal Operator)
console.log(5 === 5) // true
console.log(5 === '5') // false

// Lebih dari
console.log(5 > 3) // true

// kurang dari
console.log(5 < 3 ) // false

// Lebih dari atau sama dengan
console.log(5 >= 3) // true
